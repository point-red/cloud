<template>
  <div>
    <breadcrumb>
      <breadcrumb-finance/>
      <router-link to="/finance/cash" class="breadcrumb-item">{{ $t('cash') | titlecase }}</router-link>
      <span class="breadcrumb-item active">In</span>
    </breadcrumb>

    <tab-menu/>

    <form class="row" @submit.prevent="onSubmit">
      <p-block :title="$t('cash')" :header="true">
        <p-block-inner :is-loading="isLoading">
          <p-form-row
            id="date"
            name="date"
            :label="$t('date')">
            <div slot="body" class="col-lg-9">
              <p-date-picker
                id="date"
                name="date"
                label="Date"
                v-model="form.date"
                :errors="form.errors.get('date')"
                @errors="form.errors.set('date', null)"/>
            </div>
          </p-form-row>

          <p-form-row
            id="payment-account"
            name="payment-account"
            :label="$t('payment account')">
            <div slot="body" class="col-lg-9">
              <m-chart-of-account
                :id="'payment-account'"
                type="cash"
                v-model="form.payment_account_id"/>
            </div>
          </p-form-row>

          <p-form-row
            id="payment-from"
            name="payment-from"
            :label="$t('payment from')">
            <div slot="body" class="col-lg-9 mt-5">
              <m-paymentable id="paymentable" v-model="form.paymentable_id" @choosen="choosePaymentFrom" :label="form.paymentable_name"/>
            </div>
          </p-form-row>

          <p-separator></p-separator>

          <h5>Detail</h5>
          <hr>
          <point-table>
            <tr slot="p-head">
              <th>#</th>
              <th style="min-width: 120px">Account</th>
              <th>Notes</th>
              <th>Amount</th>
              <th style="min-width: 120px">Allocation</th>
              <th></th>
            </tr>
            <tr slot="p-body" v-for="(row, index) in form.details" :key="index">
              <th>{{ index + 1 }}</th>
              <td>
                <m-chart-of-account
                  :id="'item-' + index"
                  :data-index="index"
                  v-model="row.item_id"
                  @choosen="chooseAccount($event, row)"
                  type="other income"
                  :label="row.item_name"/>
              </td>
              <td>
                <p-form-input
                  :id="'notes-' + index"
                  :name="'notes-' + index"
                  v-model="row.notes"/>
              </td>
              <td>
                <p-form-number
                  :id="'amount-' + index"
                  :name="'amount-' + index"
                  v-model="row.amount"
                  @keyup.native="calculate()"/>
              </td>
              <td>
                <m-allocation
                  :id="'allocation-' + index"
                  v-model="row.allocation_id"
                  :label="row.allocation_name"
                  @choosen="chooseAllocation($event, row)"/>
              </td>
              <td>
                <i class="btn btn-sm fa fa-times" @click="deleteRow(index)"></i>
              </td>
            </tr>
            <tr slot="p-body">
              <th></th>
              <td></td>
              <td></td>
              <td>
                <p-form-number
                  :id="'amount'"
                  :name="'amount'"
                  :readonly="true"
                  v-model="form.amount"/>
              </td>
              <td></td>
            </tr>
          </point-table>
          <button type="button" class="btn btn-sm btn-secondary" @click="addRow">
            <i class="fa fa-plus"/> {{ $t('add') | uppercase }}
          </button>

          <p-separator></p-separator>

          <div class="row">
            <div class="col-sm-12">
              <h4>Notes</h4>
              <hr/>
              <textarea rows="10" class="form-control" placeholder="Notes" v-model="form.notes"></textarea>
            </div>
          </div>

          <p-separator></p-separator>

          <div class="form-group row">
            <div class="col-md-12">
              <button type="submit" class="btn btn-sm btn-primary" :disabled="isSaving">
                <i v-show="isSaving" class="fa fa-asterisk fa-spin"/> {{ $t('save') | uppercase }}
              </button>
            </div>
          </div>
        </p-block-inner>
      </p-block>
    </form>
  </div>
</template>

<script>
import debounce from 'lodash/debounce'
import TabMenu from './TabMenu'
import Breadcrumb from '@/views/Breadcrumb'
import BreadcrumbFinance from '../Breadcrumb'
import Form from '@/utils/Form'
import PointTable from 'point-table-vue'
import { mapActions } from 'vuex'

export default {
  components: {
    TabMenu,
    PointTable,
    Breadcrumb,
    BreadcrumbFinance
  },
  data () {
    return {
      isSaving: false,
      isLoading: false,
      form: new Form({
        increment_group: this.$moment().format('YYYYMM'),
        date: this.$moment().format('YYYY-MM-DD HH:mm:ss'),
        due_date: this.$moment().format('YYYY-MM-DD HH:mm:ss'),
        payment_type: 'cash',
        payment_account_id: null,
        paymentable_id: null,
        paymentable_type: null,
        paymentable_name: null,
        disbursed: false,
        notes: null,
        amount: 0,
        details: [{
          chart_of_account_id: null,
          chart_of_account_name: null,
          amount: null,
          allocation_id: null,
          allocation_name: null,
          notes: null
        }]
      })
    }
  },
  watch: {
    'form.date': function () {
      this.form.due_date = this.form.date
    }
  },
  methods: {
    ...mapActions('financePayment', ['create']),
    addRow () {
      this.form.details.push({
        chart_of_account_id: null,
        chart_of_account_name: null,
        amount: null,
        allocation_id: null,
        allocation_name: null,
        notes: null
      })
    },
    deleteRow (index) {
      this.$delete(this.form.details, index)
    },
    chooseAllocation (allocation, row) {
      row.allocation_name = allocation
    },
    chooseAccount (account, row) {
      row.chart_of_account_id = account.id
      row.chart_of_account_name = account.label
    },
    choosePaymentFrom (choosen) {
      this.form.paymentable_id = choosen.id
      this.form.paymentable_name = choosen.label
      this.form.paymentable_type = choosen.type
    },
    calculate: debounce(function () {
      var totalAmount = 0
      this.form.details.forEach(function (element) {
        totalAmount += parseFloat(element.amount)
      })
      this.form.amount = totalAmount
    }, 300),
    onSubmit () {
      this.isSaving = true
      this.form.increment_group = this.$moment(this.form.date).format('YYYYMM')
      this.create(this.form)
        .then(response => {
          this.isSaving = false
          this.$notification.success('create success')
          Object.assign(this.$data, this.$options.data.call(this))
          this.$router.push('/finance/cash')
        }).catch(error => {
          this.isSaving = false
          this.$notification.error(error.message)
          this.form.errors.record(error.errors)
        })
    }
  },
  created () {
  }
}
</script>
