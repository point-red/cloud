<template>
  <div>
    <breadcrumb>
      <breadcrumb-purchase/>
      <router-link to="/purchase/order" class="breadcrumb-item">{{ $t('purchase order') | titlecase }}</router-link>
      <span class="breadcrumb-item active">Create</span>
    </breadcrumb>

    <purchase-menu/>

    <form @submit.prevent="onSubmit">
      <div class="row">
        <p-block>
          <p-block-inner :is-loading="isLoading">
            <div class="row">
              <div class="col-sm-12">
                <h4 class="text-center">{{ $t('purchase order') | uppercase }}</h4>
                <hr>
                <div class="float-sm-right text-right">
                  <h6 class="mb-0">{{ authUser.tenant_name | uppercase }}</h6>
                  <template v-if="authUser.branch">
                    {{ authUser.branch.address | uppercase }} <br v-if="authUser.branch.address">
                    {{ authUser.branch.phone | uppercase }} <br v-if="authUser.branch.phone">
                  </template>
                </div>
                <div class="float-sm-left">
                  <h6 class="mb-0 ">{{ $t('supplier') | uppercase }}</h6>
                  <span @click="$refs.supplier.open()" class="select-link">{{ form.supplier_label || $t('select') | uppercase }}</span>
                  <div style="font-size:12px" v-if="form.supplier_phone">
                    {{ form.supplier_address | uppercase }} <br v-if="form.supplier_email">
                    {{ form.supplier_phone }} <br v-if="form.supplier_phone">
                    {{ form.supplier_email | uppercase }}
                  </div>
                </div>
              </div>
            </div>
            <hr>
            <p-form-row
              id="date"
              name="date"
              :label="$t('date')">
              <div slot="body" class="col-lg-9">
                <p-date-picker
                  id="date"
                  name="date"
                  label="Date"
                  v-model="form.date"
                  :errors="form.errors.get('date')"
                  @errors="form.errors.set('date', null)"/>
              </div>
            </p-form-row>
            <hr>
            <p-form-row
              id="purchase-request"
              name="purchase-request"
              :label="$t('purchase request')">
              <div slot="body" class="col-lg-9">
                <span @click="$refs.selectPurchaseRequest.open()" class="select-link">
                  <template v-if="purchaseRequest">
                    {{ purchaseRequest.form.number }}
                  </template>
                  <template v-else>
                    {{ form.supplier_label || $t('select') | uppercase }}
                  </template>
                </span>
              </div>
            </p-form-row>

            <p-separator></p-separator>

            <h5>Item</h5>
            <hr>
            <point-table>
              <tr slot="p-head">
                <th>#</th>
                <th style="min-width: 120px">Item</th>
                <th>Notes</th>
                <th>Quantity</th>
                <th>Price</th>
                <th>Discount</th>
                <th>Total</th>
                <th>
                  <button type="button" class="btn btn-sm btn-outline-secondary" @click="toggleMore()">
                    <i class="fa fa-ellipsis-h"/>
                  </button>
                </th>
              </tr>
              <template v-for="(row, index) in form.items">
                <tr slot="p-body" :key="index">
                  <th>{{ index + 1 }}</th>
                  <td>
                    <span @click="$refs.item.open(index)" class="select-link">
                      {{ row.item_label || $t('select') | uppercase }}
                    </span>
                  </td>
                  <td>
                    <p-form-input
                      :id="'notes-' + index"
                      :name="'notes-' + index"
                      v-model="form.items[index].notes"/>
                  </td>
                  <td>
                    <p-quantity
                        :id="'quantity' + index"
                        :name="'quantity' + index"
                        :disabled="row.item_id == null"
                        v-model="row.quantity"
                        :item-id="row.item_id"
                        :units="row.units"
                        :unit="{
                          name: row.unit,
                          label: row.unit,
                          converter: row.converter
                        }"
                        @choosen="chooseUnit($event, row)"/>
                  </td>
                  <td>
                    <p-form-number
                      :id="'price' + index"
                      :name="'price' + index"
                      v-model="form.items[index].price"
                      @keyup.native="calculate()"/>
                  </td>
                  <td>
                    <p-discount
                      :id="'discount' + index"
                      :name="'discount' + index"
                      v-model="form.items[index].discount_percent"
                      @keyup.native="calculate()"/>
                  </td>
                  <td>
                    <p-form-number
                      :id="'total-' + index"
                      :name="'total-' + index"
                      :readonly="true"
                      v-model="form.items[index].total"/>
                  </td>
                  <td>
                    <button type="button"
                      class="btn btn-sm btn-outline-secondary"
                      @click="row.more = !row.more"
                      v-if="!isSaving">
                      <i class="fa fa-ellipsis-h"/>
                    </button>
                  </td>
                </tr>
                <template v-if="row.more && row.item_id">
                  <tr slot="p-body" :key="'ext-'+index" class="bg-gray-light">
                    <th class="bg-gray-light"></th>
                    <td colspan="4">
                      <p-form-row
                        id="allocation"
                        name="allocation"
                        :label="$t('allocation')">
                        <m-allocation
                          slot="body"
                          class="mt-5"
                          :id="'allocation-' + index"
                          v-model="row.allocation_id"/>
                      </p-form-row>
                    </td>
                    <td></td>
                  </tr>
                </template>
              </template>
              <tr slot="p-body">
                <th></th>
                <td></td>
                <td></td>
                <td></td>
                <td></td>
                <td></td>
                <td>
                  <p-form-number
                    :id="'subtotal'"
                    :name="'subtotal'"
                    :readonly="true"
                    v-model="form.subtotal"/>
                </td>
              </tr>
            </point-table>

            <p-separator></p-separator>

            <div class="row">
              <div class="col-sm-6">
                <textarea rows="10" class="form-control" placeholder="Notes" v-model="form.notes"></textarea>
              </div>
              <div class="col-sm-6">
                <p-form-row
                  id="discount"
                  name="discount"
                  :label="$t('discount')">
                  <div slot="body" class="col-lg-9 mt-5">
                    <p-discount
                      id="discount"
                      name="discount"
                      v-model="form.discount_percent"
                      @keyup.native="calculate()"/>
                  </div>
                </p-form-row>
                <p-form-row
                  name="need-down-payment"
                  :label="$t('tax')">
                  <div slot="body" class="col-lg-9">
                    <p-form-check-box
                      class="mb-0"
                      style="float:left"
                      name="need-down-payment"
                      @click.native="chooseTax('include')"
                      :checked="form.type_of_tax == 'include'"
                      :description="$t('include tax')"/>
                    <p-form-check-box
                      name="need-down-payment"
                      @click.native="chooseTax('exclude')"
                      :checked="form.type_of_tax == 'exclude'"
                      :description="$t('exclude tax')"/>
                    <p-form-number
                      :id="'total'"
                      :name="'total'"
                      :readonly="true"
                      v-model="form.tax"/>
                  </div>
                </p-form-row>
                <p-form-row
                  id="total"
                  name="total"
                  :label="$t('total')">
                  <div slot="body" class="col-lg-9 mt-5">
                    <p-form-number
                      :id="'total'"
                      :name="'total'"
                      :readonly="true"
                      v-model="form.amount"/>
                  </div>
                </p-form-row>
              </div>
            </div>

            <p-separator></p-separator>

            <h5>Options</h5>
            <hr>
            <div class="row">
              <div class="col-sm-6">
                <p-form-row
                  id="need-down-payment"
                  name="need-down-payment"
                  :help="'* surat jalan bisa dibuat setelah pembayaran uang muka'"
                  :label="$t('require down payment')">
                  <div slot="body" class="col-lg-9">
                    <p-form-number
                      id="need-down-payment"
                      name="need-down-payment"
                      :is-text-right="false"
                      v-model="form.need_down_payment"/>
                  </div>
                </p-form-row>
              </div>
              <div class="col-sm-6">
                <p-form-row
                  id="cash-only"
                  name="cash-only"
                  :label="$t('cash only')">
                  <div slot="body" class="col-lg-9">
                    <p-form-check-box
                      id="cash-only"
                      name="cash-only"
                      @click.native="form.cash_only = !form.cash_only"
                      :checked="form.cash_only"
                      :description="'surat jalan dapat dibuat sebesar jumlah pembayaran'"/>
                  </div>
                </p-form-row>
              </div>
            </div>

            <p-separator></p-separator>

            <div class="row mt-50">
              <div class="col-sm-6">
                <textarea rows="5" class="form-control" placeholder="Notes" v-model="form.notes"></textarea>
                <div class="d-sm-block d-md-none mt-10"></div>
              </div>
              <div class="col-sm-3 text-center">
                <h6 class="mb-0">{{ $t('requested by') | uppercase }}</h6>
                <div class="mb-50" style="font-size:11px">{{ Date.now() | dateFormat('DD MMMM YYYY') }}</div>
                {{ requestedBy | uppercase }}
                <div class="d-sm-block d-md-none mt-10"></div>
              </div>
              <div class="col-sm-3 text-center">
                <h6 class="mb-0">{{ $t('approved by') | uppercase }}</h6>
                <div class="mb-50" style="font-size:11px">_______________</div>
                <span @click="$refs.approver.open()" class="select-link">{{ form.approver_email || $t('select') | uppercase }}</span>
              </div>

              <div class="col-sm-12">
                <hr>
                <button type="submit" class="btn btn-sm btn-primary" :disabled="isSaving">
                  <i v-show="isSaving" class="fa fa-asterisk fa-spin"/> {{ $t('save') | uppercase }}
                </button>
              </div>
            </div>
          </p-block-inner>
        </p-block>
      </div>
    </form>
    <m-supplier ref="supplier" @choosen="chooseSupplier"/>
    <m-item ref="item" @choosen="chooseItem"/>
    <m-user ref="approver" @choosen="chooseApprover"/>
    <select-purchase-request ref="selectPurchaseRequest" @choosen="choosePurchaseRequest"></select-purchase-request>
  </div>
</template>

<script>
import debounce from 'lodash/debounce'
import PurchaseMenu from '../Menu'
import Breadcrumb from '@/views/Breadcrumb'
import BreadcrumbPurchase from '@/views/purchase/Breadcrumb'
import Form from '@/utils/Form'
import PointTable from 'point-table-vue'
import SelectPurchaseRequest from './SelectPurchaseRequest'
import { mapGetters, mapActions } from 'vuex'

export default {
  components: {
    PurchaseMenu,
    PointTable,
    Breadcrumb,
    BreadcrumbPurchase,
    SelectPurchaseRequest
  },
  data () {
    return {
      isSaving: false,
      isLoading: false,
      requestedBy: localStorage.getItem('userName'),
      totalPrice: 0,
      purchaseRequest: null,
      form: new Form({
        increment_group: this.$moment().format('YYYYMM'),
        date: this.$moment().format('YYYY-MM-DD HH:mm:ss'),
        supplier_id: null,
        supplier_name: null,
        supplier_label: null,
        supplier_address: null,
        supplier_phone: null,
        supplier_email: null,
        need_down_payment: 0,
        cash_only: false,
        notes: null,
        subtotal: 0,
        discount_percent: 0,
        discount_value: 0,
        tax_base: 0,
        tax: 0,
        type_of_tax: 'exclude',
        amount: 0,
        items: [],
        request_approval_to: null,
        approver_name: null,
        approver_email: null,
        purchase_request_id: null
      })
    }
  },
  computed: {
    ...mapGetters('purchaseOrder', ['purchaseOrder']),
    ...mapGetters('auth', ['authUser'])
  },
  methods: {
    ...mapActions('purchaseOrder', ['create']),
    addItemRow () {
      this.form.items.push({
        item_id: null,
        item_name: null,
        more: false,
        unit: null,
        converter: null,
        quantity: null,
        price: null,
        discount_percent: 0,
        discount_value: 0,
        total: null,
        allocation_id: null,
        allocation_name: null,
        notes: null
      })
    },
    toggleMore () {
      let isMoreActive = this.form.items.some(function (el, index) {
        return el.more === false
      })
      this.form.items.forEach(element => {
        element.more = isMoreActive
      })
    },
    clearItem (index) {
      this.form.items.splice(index, 1)
      if (this.form.items.length === 0) {
        this.addItemRow()
      }
    },
    chooseApprover (value) {
      this.form.request_approval_to = value.id
      this.form.approver_name = value.name
      this.form.approver_email = value.email
    },
    chooseSupplier (value) {
      this.form.supplier_id = value.id
      this.form.supplier_name = value.name
      this.form.supplier_label = value.label
      this.form.supplier_address = value.address
      this.form.supplier_phone = value.phone
      this.form.supplier_email = value.email
    },
    chooseItem (item) {
      if (item.id == null) {
        this.clearItem(item.index)
        return
      }

      let row = this.form.items[item.index]
      row.item_id = item.id
      row.item_name = item.name
      row.item_label = item.label
      row.units = item.units
      row.units.forEach((unit, keyUnit) => {
        if (unit.id == item.unit_default_purchase) {
          row.unit = unit.label
          row.converter = unit.converter
        }
      })
      let isNeedNewRow = true
      this.form.items.forEach(element => {
        if (element.item_id == null) {
          isNeedNewRow = false
        }
      })
      if (isNeedNewRow) {
        this.addItemRow()
      }
    },
    chooseUnit (unit, row) {
      row.unit = unit.label
      row.converter = unit.converter
    },
    chooseAllocation (allocation, row) {
      row.allocation_name = allocation
    },
    chooseTax (taxType) {
      if (taxType == this.form.type_of_tax) {
        this.form.type_of_tax = null
      } else {
        this.form.type_of_tax = taxType
      }
      this.calculate()
    },
    choosePurchaseRequest (purchaseRequest) {
      this.purchaseRequest = purchaseRequest
      this.form.purchase_request_id = purchaseRequest.id
      this.form.items = purchaseRequest.items.map(item => {
        let total = item.quantity * (item.price - item.discount_value)
        return {
          item_id: item.item_id,
          item_name: item.item.name,
          item_label: item.item.name,
          more: false,
          unit: item.unit,
          converter: item.converter,
          quantity: item.quantity,
          price: item.price,
          discount_percent: item.discount_percent,
          discount_value: item.discount_value,
          total,
          allocation_id: item.allocation_id,
          allocation_name: null, // TODO get alocation name
          notes: item.notes
        }
      })
      this.addItemRow()
    },
    calculate: debounce(function () {
      var subtotal = 0
      this.form.items.forEach(function (element) {
        element.allocation_name = ''
        if (element.allocation) {
          element.allocation_name = element.allocation.name
        }
        element.total = element.quantity * (element.price - (element.price * element.discount_percent / 100))
        element.discount_value = element.discount_percent * element.price / 100
        subtotal += parseFloat(element.total)
      })
      this.form.subtotal = subtotal
      this.form.discount_value = this.form.discount_percent * subtotal / 100
      this.form.tax_base = this.form.subtotal - (this.form.subtotal * this.form.discount_percent / 100)
      if (this.form.type_of_tax == 'include') {
        this.form.tax = this.form.tax_base * 10 / 100
        this.form.amount = this.form.tax_base
      } else if (this.form.type_of_tax == 'exclude') {
        this.form.tax = this.form.tax_base * 10 / 100
        this.form.amount = this.form.tax_base + this.form.tax
      } else {
        this.form.tax = 0
        this.form.amount = this.form.tax_base
      }
    }, 300),
    onSubmit () {
      this.isSaving = true
      this.form.increment_group = this.$moment(this.form.date).format('YYYYMM')
      this.form.items = this.form.items.filter(item => item.item_id !== null)
      this.create(this.form)
        .then(response => {
          this.isSaving = false
          this.$notification.success('create success')
          Object.assign(this.$data, this.$options.data.call(this))
          this.$router.push('/purchase/order/' + response.data.id)
        }).catch(error => {
          this.isSaving = false
          this.$notification.error(error.message)
          this.form.errors.record(error.errors)
        })
    }
  },
  created () {
    this.addItemRow()

    if (this.$route.query.id) {
      this.isLoading = true
      this.find({
        id: this.$route.query.id,
        params: {
          includes: 'form;supplier;items.item.units;items.allocation;services.service;services.allocation'
        }
      }).then(response => {
        this.isLoading = false
        this.form.purchase_request_id = response.data.id
        this.form.date = response.data.form.date
        this.form.supplier_id = response.data.supplier_id
        this.form.supplier_name = response.data.supplier_name
        this.form.notes = response.data.form.notes
        this.form.amount = response.data.amount
        this.form.items = response.data.items
        this.form.items.forEach(function (element) {
          element.purchase_request_item_id = element.id
        })
        this.calculate()
      }).catch(error => {
        this.isLoading = false
        this.$notification.error(error.message)
      })
    }
  }
}
</script>
