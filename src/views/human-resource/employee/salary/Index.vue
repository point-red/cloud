<template>
  <div>
    <breadcrumb>
      <breadcrumb-human-resource />
      <router-link
        to="/human-resource/employee"
        class="breadcrumb-item"
      >
        {{ $t('employee') | titlecase }}
      </router-link>
      <router-link
        :to="'/human-resource/employee/' + employee.id"
        class="breadcrumb-item"
      >
        {{ employee.name | titlecase }}
      </router-link>
      <span class="breadcrumb-item active">{{ title | titlecase }}</span>
    </breadcrumb>

    <employee-widget :id="id" />

    <tab-menu />

    <div class="row">
      <p-block
        :title="title"
        :header="true"
      >
        <p-block-inner :is-loading="isLoading">
          <p-table>
            <tr slot="p-head">
              <th>{{ $t('date') }}</th>
              <th class="text-center">
                {{ $t('salary') }}
              </th>
            </tr>
            <tr
              v-for="(salary, salaryIndex) in salaries"
              :key="salary.id"
              slot="p-body"
            >
              <td>
                <router-link :to="{ name: 'humanResourceEmployeeSalaryShow', params: { id: employee.id, salaryId: salary.id }}">
                  {{ dataSet.startDates[salaryIndex] | dateFormat('DD MMMM YYYY') }} - {{ dataSet.endDates[salaryIndex] | dateFormat('DD MMMM YYYY') }}
                </router-link>
              </td>
              <td class="text-center">
                Rp {{ dataSet.scores[salaryIndex] | numberFormat }}
              </td>
              <td class="text-right">
                <router-link
                  v-if="$permission.has('update employee salary')"
                  :to="{ path: '/human-resource/employee/' + employee.id + '/salary/' + salary.id + '/edit', params: { id: employee.id, kpiId: salary.id }}"
                  class="btn btn-sm btn-secondary"
                >
                  <i class="si si-note" /> {{ $t('edit') | uppercase }}
                </router-link>
                <button :disabled="isExporting.includes(salary.id)" type="submit" class="btn btn-sm btn-primary" @click="exportDataPDF(salary.id)" style="margin-left:12px" v-if="$permission.has('export employee salary pdf')">
                  <i v-show="isExporting.includes(salary.id)" class="fa fa-asterisk fa-spin" /> PDF
                </button>
                <button :disabled="isExporting.includes(salary.id)" type="submit" class="btn btn-sm btn-primary" @click="exportDataExcel(salary.id)" style="margin-left:12px" v-if="$permission.has('export employee salary excel')">
                  <i v-show="isExporting.includes(salary.id)" class="fa fa-asterisk fa-spin" /> Excel
                </button>
                &nbsp;
                <i
                  v-show="$permission.has('delete employee salary')"
                  class="fa fa-close"
                  @click="deleteSalary(salary.id)"
                />
              </td>
            </tr>
          </p-table>
        </p-block-inner>
      </p-block>
    </div>

    <p-modal
      id="modal-delete"
      ref="delete"
      title="Confirmation"
    >
      <div slot="content">
        <p>Are you sure want to delete the Salary Form?</p>
      </div>
      <div slot="footer">
        <button
          type="button"
          class="btn btn-alt-secondary"
          data-dismiss="modal"
          @click="$refs.delete.close()"
        >
          {{ $t('cancel') | uppercase }}
        </button>
        &nbsp;
        <button
          v-if="$permission.has('delete employee salary')"
          type="button"
          :disabled="isSaving"
          class="btn btn-danger"
          @click="onDelete()"
        >
          <i
            v-show="isSaving"
            class="fa fa-asterisk fa-spin"
          /> {{ $t('delete') | uppercase }}
        </button>
      </div>
    </p-modal>
  </div>
</template>

<script>
import TabMenu from '../TabMenu'
import Breadcrumb from '@/views/Breadcrumb'
import BreadcrumbHumanResource from '@/views/human-resource/Breadcrumb'
import EmployeeWidget from '../EmployeeWidget'
import { mapGetters, mapActions } from 'vuex'

export default {
  components: {
    TabMenu,
    Breadcrumb,
    BreadcrumbHumanResource,
    EmployeeWidget
  },
  data () {
    return {
      id: this.$route.params.id,
      title: 'Salary',
      isLoading: true,
      isSaving: false,
      selectedSalaryId: '',
      isExporting: []
    }
  },
  computed: {
    ...mapGetters('humanResourceEmployee', ['employee']),
    ...mapGetters('humanResourceEmployeeSalary', ['salaries', 'dataSet'])
  },
  created () {
    this.isLoading = true
    this.getEmployeeSalary({
      employeeId: this.id
    }).then((response) => {
      this.isLoading = false
    }, (error) => {
      console.log(JSON.stringify(error))
    })
  },
  methods: {
    ...mapActions('humanResourceEmployeeSalary', {
      getEmployeeSalary: 'get',
      deleteEmployeeSalary: 'delete',
      exportPDF: 'exportPDF',
      exportExcel: 'exportExcel'
    }),
    deleteSalary (salaryId) {
      this.selectedSalaryId = salaryId
      this.$refs.delete.show()
    },
    onDelete () {
      this.isSaving = true
      this.deleteEmployeeSalary({ id: this.selectedSalaryId, employeeId: this.id })
        .then((response) => {
          this.isSaving = false
          this.$refs.delete.close()
        }, (error) => {
          this.isSaving = false
          this.$notification.error('Delete failed', error.message)
          console.log(JSON.stringify(error))
        })
    },
    exportDataPDF (value) {
      this.isExporting.push(value)
      this.exportPDF({
        id: value,
        employeeId: this.id
      }).then((response) => {
        this.isExporting.splice(this.isExporting.indexOf(value), 1)
        this.downloadLink = response.data.url
        window.open(response.data.url, '_blank')
      }, (error) => {
        this.isExporting.splice(this.isExporting.indexOf(value), 1)
        console.log(error)
      })
    },
    exportDataExcel (value) {
      this.isExporting.push(value)
      this.exportExcel({
        id: value,
        employeeId: this.id
      }).then((response) => {
        this.isExporting.splice(this.isExporting.indexOf(value), 1)
        this.downloadLink = response.data.url
        window.open(response.data.url, '_blank')
      }, (error) => {
        this.isExporting.splice(this.isExporting.indexOf(value), 1)
        console.log(error)
      })
    }
  }
}
</script>
